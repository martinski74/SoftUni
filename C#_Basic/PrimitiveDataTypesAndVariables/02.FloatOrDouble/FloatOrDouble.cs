using System;

class FloatOrDouble
{
    static void Main()
    {
        double a = 34.567839023;
        float b = 12.345f;
        double c = 8923.1234857;
        float d = 3456.091f;
        Console.WriteLine("float-{0}",a);
        Console.WriteLine("double-{0}",b);
        Console.WriteLine("double-{0}",c);
        Console.WriteLine("float-{0}",d);
    }
}

